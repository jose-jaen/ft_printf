#include "libft.h"

int	ft_digit_count(int nb)
{
	int	n;

	if (nb == -2147483648)
		return (11);
	n = 1;
	if (nb < 0)
	{
		nb++;
		nb *= -1;
	}
	while (n >= 10)
	{
		n++;
		nb /= 10;
	}
	return (n);
}
